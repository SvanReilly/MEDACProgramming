package tableGames;

public class Casilla3Raya {

	private int x;
	private int y;
	private String content;
	
	
	public Casilla3Raya(int x, int y) {
		this.x = x;
		this.y = y;
		this.content="";
	}


	public int getX() {
		return x;
	}


	public void setX(int x) {
		this.x = x;
	}


	public int getY() {
		return y;
	}


	public void setY(int y) {
		this.y = y;
	}


	public String getContent() {
		return content;
	}


	public void setContent(String content) {
		this.content = content;
	}
	
	
	
	
	

}
